class BasicForDemo9{
    public static void main(String[] args){
        //Check of three numbers are pythagorean triplets or not(a*a+b*b=c*c)
		int a=8,b=6,c=10;
		if(a*a+b*b==c*c){
			System.out.println("They are pythagorean numbers");
		} else{
			System.out.println("They are not pythagorean numbers");
		}
    }
}